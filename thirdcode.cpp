//How much space does following data types take ?
//int
//bool
//float

#include <iostream>
using namespace std;
int main() {
    cout << "Size of int: " << sizeof(int) << " bytes\n";
    cout << "Size of bool: " << sizeof(bool) << " bytes\n";
    cout << "Size of float: " << sizeof(float) << " bytes\n";

    return 0;
}